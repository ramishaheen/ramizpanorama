export function getCountryGeoJSON(countryCodes: string[]): SimpleGeoJSON {
  const features = countryCodes
    .filter(code => countryBorders[code])
    .map(code => ({
      type: "Feature" as const,
      properties: { code },
      geometry: {
        type: "Polygon" as const,
        // GeoJSON uses [lng, lat] not [lat, lng]
        coordinates: [countryBorders[code].map(([lat, lng]) => [lng, lat])],
      },
    }));

  return {
    type: "FeatureCollection",
    features,
  };
}

